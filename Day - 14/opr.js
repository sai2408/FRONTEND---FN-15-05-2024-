//Arithematic Operators
console.log("Arithematic Operators");
var a = 10;
var b = 5;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

//Assignment Operators
console.log("Assignment Operators");
var a = 10;
a = a + 2
console.log(a);
a -= 2;
console.log(a);
a *= 2;
console.log(a);
a /= 3;
console.log(a);
a *=3;
console.log(a);
a %= 3;
console.log(a);
a **=3;
console.log(a);

//Comparision Operators
console.log("Comparision Operators");
a = 10;
b = 5;
console.log(a==b);
console.log(a===b);
console.log(a!=b);
console.log(a!==b);
console.log(a>b);
console.log(a>=b);
console.log(a<b);
console.log(a<=b);

//Logical Operators
console.log("Logical Operators");
a = 10;
b = 5;
console.log((a>b) && (a<b));
console.log((a>b) || (a<b));
console.log(!(a>=b));

//Unary Operators
console.log("Unary Operators");
a = 5;
console.log(-a);
console.log(+a);

//Incerement Operators
console.log("Incerement Operators");
a = 10;
console.log(a++);
console.log(++a);

//Decerement Operators
console.log("Decerement Operators");
a = 10;
console.log(a--);
console.log(--a);

//String Concatination Operator
console.log("String Concatination Operator");
var a = "Sai ";
